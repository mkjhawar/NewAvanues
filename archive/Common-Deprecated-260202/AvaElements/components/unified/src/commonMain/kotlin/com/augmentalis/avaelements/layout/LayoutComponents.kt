/**
 * Layout Components Package
 *
 * Generic, un-branded layout components for arranging UI elements.
 * These components have NO Magic* prefix and are intended for universal layout needs.
 *
 * @since 3.0.0
 */
@file:JvmName("LayoutComponents")
package com.augmentalis.avaelements.layout

// Re-export layout components for convenient wildcard imports
// Usage: import com.augmentalis.avaelements.layout.*

/**
 * Available Layout Components:
 *
 * - Container: Flexible wrapper component with alignment and styling
 * - Row: Horizontal layout arranging children left-to-right
 * - Column: Vertical layout arranging children top-to-bottom
 * - Stack: Z-index stacking layout
 * - Padding: Wrapper adding padding around child
 * - Align: Alignment wrapper
 * - Center: Centering wrapper
 * - SizedBox: Fixed-size box wrapper
 * - Flexible: Flex child with weight
 * - Expanded: Auto-expanding flex child
 * - Positioned: Absolute positioning in Stack
 * - FittedBox: Fit/scale content wrapper
 * - Wrap: Wrapping flow layout
 * - Spacer: Empty spacing utility
 * - Grid: Grid layout
 * - ConstrainedBox: Constraint wrapper
 */
